    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int linhaSelecionada = tableClientes.getSelectedRow();

        String nome   = txtNome.getText();
        String cpf    = txtCPF.getText();
        String tel    = txtTel.getText();
        String end    = txtEnd.getText();
        String numero = txtNumero.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        
            
        if (linhaSelecionada >= 0) {
            int result = JOptionPane.showConfirmDialog(this, "Deseja alterar este cliente", "Cuidado",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if (result == JOptionPane.YES_OPTION) {
                Cliente cliente = new Cliente(nome,cpf,tel,end,numero,cidade,estado);
                this.clienteDAO.alterar(cliente);
                modelo.removeRow(linhaSelecionada);
                
                JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
                modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getTel(), cliente.getEnd()});
                limparCampos();
            }
        } else {
                JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado", "Sucesso",JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_btnAtualizarActionPerformed

