    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome   = txtNome.getText();
        String cpf    = txtCPF.getText();
        String tel    = txtTel.getText();
        String end    = txtEnd.getText();
        String numero = txtNumero.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        
        if (!isCamposValidos(nome, cpf, tel, end, numero, cidade, estado)) {
          JOptionPane.showMessageDialog(null, "Existem campos a serem preenchidos", "Atencao",JOptionPane.INFORMATION_MESSAGE);
          return;
        }
        
        Cliente cliente = new Cliente(nome,cpf,tel,end,numero,cidade,estado);
        Boolean isCadastrado = this.clienteDAO.cadastrar(cliente);
        
        if (isCadastrado) {
            modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getTel(), cliente.getEnd()});
            limparCampos();
        } else {
            
        }
        
        
        JOptionPane.showMessageDialog(null, "Ola "+ nome, "Atencao",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSalvarActionPerformed

